// ---------------------------------
// 変数の中身をコンソール出力(const)
// [myName]という名前でコンスタント値を定義しましょう。
// コンスタント値を変更しようとするとエラーになることを確認しましょう。
// 文字列を + でつなげて、文字列組み立てができることを確認しましょう。
// 【const】
// const は一度入れた中身を変更できないので変更されたくないコンスタント値を用意するときに使います。
// 値を変更しようとするとエラーになるので変更されていないことが保証されて安心です。
// ---------------------------------
const myName = 'たなかたろう';
//myName = 'みやぎじろう';
console.log('名前は「'+myName+'」です');
